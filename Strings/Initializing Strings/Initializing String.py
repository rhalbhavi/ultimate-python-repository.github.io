### Initializing String ###

# Initialize String
string = "Hello, World!"

# Print the entire string
print("Entire string:", string)

# Print the element at index 4 (Positive indexing) -- string[index]
print("The character at index 4 is:", string[4]) # Output: o

# Print the last element (Negative indexing) -- string[-index]
print("The last character is:", string[-1]) # Output: !
