### upper(), lower(), casefold() ###


## Convert Lowercase text to Uppercase

# Initialize String
text = "text"

# String.upper() -- Converts to uppercase
text.upper()
print (text.upper()) # Output: TEXT

## ----------------------------------------------------------------------

## Convert Uppercase text to Lowercase

# Initialize String
text = "TEXT"

# String.lower() -- Converts to lowercase
text.lower()
print (text.lower()) # Output: text

## ----------------------------------------------------------------------

## Convert Entire String to Lowercase

# Initialize String
text2 = "hsdiIHIOHhi"

# String.casefold()
text2.casefold()
print(text2.casefold()) # Output: hsdiIHIOHhi
