### format() - Insert String in Specific Portion of Original String ###

# Initialize Strings
name = "Molly"
age =  30
ethnicity = "Indian"

# String.format(str1, str2)
"My name is {} and I am {} years old.".format(name,age)

print ("My name is {} and I am {} years old. I am {}.".format(name,age,ethnicity)) # Output: My name is Molly and I am 30 years old. I am Indian.
